# [u-<–Ω–æ–º–µ—Ä —Å–∏–º–≤–æ–ª–∞ –≤ —Ç–∞–±–ª–∏—Ü–µ Unicode>]
# –ì–≤–∏–¥–æ –µ—â–µ –Ω–µ –Ω–∞—É—á–∏–ª—Å—è —á–∏—Ç–∞—Ç—å —Å–∏–º–≤–æ–ª—ã –≤ —Ç–∞–∫–æ–º —Ñ–æ—Ä–º–∞—Ç–µ, –ø–æ—ç—Ç–æ–º—É –ø—Ä–æ—Å–∏—Ç –≤–∞—Å –Ω–∞–ø–∏—Å–∞—Ç—å –ø—Ä–æ–≥—Ä–∞–º–º—É,
# –∫–æ—Ç–æ—Ä–∞—è –±—É–¥–µ—Ç "—Ä–∞—Å—à–∏—Ñ—Ä–æ–≤—ã–≤–∞—Ç—å" –¥–ª—è –Ω–µ–≥–æ –≤—Å–µ —Ç–µ–∫—Å—Ç—ã –Ω–∞ –∫–æ–º–ø—å—é—Ç–µ—Ä–µ.
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞.
# –†–∞—Å—à–∏—Ñ—Ä—É–π—Ç–µ —Ç–µ–∫—Å—Ç, –∑–∞–º–µ–Ω–∏–≤ –≤—Å–µ –∫–æ–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏ [u-<–Ω–æ–º–µ—Ä —Å–∏–º–≤–æ–ª–∞ –≤ —Ç–∞–±–ª–∏—Ü–µ Unicode>] –Ω–∞ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–µ –±—É–∫–≤—ã —Ä—É—Å—Å–∫–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞,
# –∏ –≤—ã–≤–µ–¥–∏—Ç–µ –µ–≥–æ.
# –§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞, –≤ –∫–æ—Ç–æ—Ä–æ–π –º–æ–≥—É—Ç –±—ã—Ç—å –∑–∞—à–∏—Ñ—Ä–æ–≤–∞–Ω—ã —Å–∏–º–≤–æ–ª—ã —Ä—É—Å—Å–∫–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞.
# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ —Å—Ç—Ä–æ–∫—É —Ç–µ–∫—Å—Ç–∞, —Ä–∞—Å—à–∏—Ñ—Ä–æ–≤–∞–≤ —Å–∏–º–≤–æ–ª—ã —Ä—É—Å—Å–∫–æ–≥–æ –∞–ª—Ñ–∞–≤–∏—Ç–∞.
# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ë—É–¥–µ–º —Å—á–∏—Ç–∞—Ç—å, —á—Ç–æ –±—É–∫–≤—ã –Å –Ω–µ—Ç –≤ —Ä—É—Å—Å–∫–æ–º –∞–ª—Ñ–∞–≤–∏—Ç–µ. ü§´

string = input()

number = ''
letter = ''
word = ''


for i in string:
    if i.isdigit():
        number += i
    else:
        if number: # at the end number
            letter = chr(int(number))
            word += letter
            number = ''
        word += i


word = word.replace('[u-', '').replace(']', '')

print(word)



