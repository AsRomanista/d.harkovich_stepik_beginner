# <–∫–æ–¥ —Å–∏–º–≤–æ–ª–∞ –≤ —Ç–∞–±–ª–∏—Ü–µ Unicode>√ó3üêù
# –ê —Å—Ç–æ–∏–º–æ—Å—Ç—å –≤—Å–µ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è —Å–∫–ª–∞–¥—ã–≤–∞–µ—Ç—Å—è –∏–∑ —Å—É–º–º—ã —Å—Ç–æ–∏–º–æ—Å—Ç–µ–π –≤—Å–µ—Ö —Å–∏–º–≤–æ–ª–æ–≤.
# –°—ç–º—É –∑–∞—Ö–æ—Ç–µ–ª–æ—Å—å –ø–æ–¥—Å—á–∏—Ç–∞—Ç—å, —Å–∫–æ–ª—å–∫–æ üêù –æ–Ω –∑–∞—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∑–∞ —Å–≤–æ–∏ –æ—Ç–≤–µ—Ç—ã –≤ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏—è—Ö, –∏ –ø—Ä–æ—Å–∏—Ç –≤–∞—Å –ø–æ–º–æ—á—å –µ–º—É.
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞.
# –¢—Ä–µ–±—É–µ—Ç—Å—è –Ω–∞–ø–∏—Å–∞—Ç—å –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞—Ö–æ–¥–∏—Ç —Å—Ç–æ–∏–º–æ—Å—Ç—å —Å–æ–æ–±—â–µ–Ω–∏—è –°—ç–º–∞ –≤ üêù –∏ –≤—ã–≤–æ–¥–∏—Ç —Ç–µ–∫—Å—Ç –≤ —Å–ª–µ–¥—É—é—â–µ–º —Ñ–æ—Ä–º–∞—Ç–µ:
# –¢–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è: '<—Ç–µ–∫—Å—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –°—ç–º–∞>'
# –°—Ç–æ–∏–º–æ—Å—Ç—å —Å–æ–æ–±—â–µ–Ω–∏—è: <—Å—Ç–æ–∏–º–æ—Å—Ç—å —Å–æ–æ–±—â–µ–Ω–∏—è –°—ç–º–∞>üêù

